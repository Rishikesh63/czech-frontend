"use client";

import { useState } from "react";

interface Draft {
  id: string;
  question: string;
  answer_suggestion: string;
  status: "Pending" | "Approved" | "Rejected";
  sources: Source[];
}

interface Source {
  url: string;
  score: number;
}

interface QuestionSectionProps {
  question: string;
  setQuestion: (val: string) => void;
}

const API_BASE =
  process.env.NEXT_PUBLIC_API_BASE || "http://localhost:3000";

export default function QuestionSection({
  question,
  setQuestion,
}: QuestionSectionProps) {
  const [asking, setAsking] = useState(false);
  const [draft, setDraft] = useState<Draft | null>(null);
  const [message, setMessage] = useState<string | null>(null);
  const [language, setLanguage] = useState<"cs" | "en">("cs");

  // -----------------------------
  // TEXTS (Multilingual UI)
  // -----------------------------
  const texts = {
    cs: {
      placeholder: "VloÅ¾te dotaz obÄana...",
      create: "VytvoÅ™it nÃ¡vrh",
      generating: "Generuji nÃ¡vrh odpovÄ›di...",
      title: "NÃ¡vrh AI odpovÄ›di",
      sources: "Zdroje:",
      approve: "SchvÃ¡lit odpovÄ›Ä",
      reject: "ZamÃ­tnout",
      approvedMsg: "âœ“ OdpovÄ›Ä byla schvÃ¡lena a odeslÃ¡na obÄanovi.",
      rejectedMsg: "âœ• NÃ¡vrh byl zamÃ­tnut.",
      error: "Chyba pÅ™i zpracovÃ¡nÃ­ dotazu.",
      footer:
        "Tento nÃ¡vrh odpovÄ›di byl vygenerovÃ¡n pomocÃ­ AI a podlÃ©hÃ¡ schvÃ¡lenÃ­ odpovÄ›dnou osobou.",
      empty: "VloÅ¾te dotaz obÄana pro vytvoÅ™enÃ­ nÃ¡vrhu odpovÄ›di.",
    },
    en: {
      placeholder: "Enter citizen question...",
      create: "Generate Draft",
      generating: "Generating draft response...",
      title: "AI Draft Reply",
      sources: "Sources:",
      approve: "Approve",
      reject: "Reject",
      approvedMsg: "âœ“ Reply approved and sent to citizen.",
      rejectedMsg: "âœ• Draft rejected.",
      error: "Error while processing the request.",
      footer:
        "This response draft was generated by AI and requires approval by responsible authority.",
      empty: "Enter a citizen question to generate a draft reply.",
    },
  };

  const t = texts[language];

  // --------------------------------------------------
  // CREATE DRAFT
  // --------------------------------------------------
  const ask = async () => {
    if (!question.trim()) return;

    setAsking(true);
    setMessage(null);
    setDraft(null);

    try {
      const res = await fetch(`${API_BASE}/process-question`, {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
        },
        body: JSON.stringify({
          question,
          language, // send language to backend
        }),
      });

      if (!res.ok) {
        throw new Error("Process-question failed");
      }

      const data = await res.json();

      const draftRes = await fetch(
        `${API_BASE}/draft/${data.draft_id}`
      );

      if (!draftRes.ok) {
        throw new Error("Draft fetch failed");
      }

      const draftData = await draftRes.json();

      setDraft(draftData);
      setQuestion("");
    } catch (err) {
      console.error(err);
      setMessage(t.error);
    }

    setAsking(false);
  };

  // --------------------------------------------------
  // APPROVE
  // --------------------------------------------------
  const approve = async () => {
    if (!draft) return;

    try {
      const res = await fetch(
        `${API_BASE}/draft/${draft.id}/approve`,
        { method: "PUT" }
      );

      if (!res.ok) throw new Error("Approve failed");

      const updatedDraft = await res.json();
      setDraft(updatedDraft);
      setMessage(t.approvedMsg);
    } catch (err) {
      console.error(err);
      setMessage(t.error);
    }
  };

  // --------------------------------------------------
  // REJECT
  // --------------------------------------------------
  const reject = async () => {
    if (!draft) return;

    try {
      const res = await fetch(
        `${API_BASE}/draft/${draft.id}/reject`,
        { method: "PUT" }
      );

      if (!res.ok) throw new Error("Reject failed");

      const updatedDraft = await res.json();
      setDraft(updatedDraft);
      setMessage(t.rejectedMsg);
    } catch (err) {
      console.error(err);
      setMessage(t.error);
    }
  };

  return (
    <div className="flex flex-col h-[80vh] bg-white rounded-xl shadow-sm border border-gray-200 overflow-hidden">
      {/* Language Toggle */}
      <div className="flex justify-end p-3 border-b bg-gray-50">
        <div className="flex gap-2">
          <button
            onClick={() => setLanguage("cs")}
            className={`px-3 py-1 rounded-lg text-sm ${
              language === "cs"
                ? "bg-blue-600 text-white"
                : "bg-gray-200 text-gray-700"
            }`}
          >
            ğŸ‡¨ğŸ‡¿ ÄŒesky
          </button>

          <button
            onClick={() => setLanguage("en")}
            className={`px-3 py-1 rounded-lg text-sm ${
              language === "en"
                ? "bg-blue-600 text-white"
                : "bg-gray-200 text-gray-700"
            }`}
          >
            ğŸ‡¬ğŸ‡§ English
          </button>
        </div>
      </div>

      {/* Draft Display Area */}
      <div className="flex-1 overflow-y-auto p-6">
        {!draft && !asking && (
          <div className="h-full flex flex-col items-center justify-center text-gray-400">
            <span className="text-4xl mb-2">ğŸ›</span>
            <p>{t.empty}</p>
          </div>
        )}

        {asking && (
          <div className="text-gray-500 animate-pulse">
            {t.generating}
          </div>
        )}

        {draft && (
          <div className="space-y-4">
            <h2 className="text-xl font-bold text-black">
              {t.title}
            </h2>

            <p className="text-gray-800 whitespace-pre-wrap">
              {draft.answer_suggestion}
            </p>

            {draft.sources?.length > 0 && (
              <div className="text-sm mt-4">
                <p className="font-semibold text-gray-500 mb-2">
                  {t.sources}
                </p>
                <div className="flex flex-wrap gap-2">
                  {draft.sources.map((s, i) => (
                    <a
                      key={i}
                      href={s.url}
                      target="_blank"
                      rel="noopener noreferrer"
                      className="bg-gray-100 px-2 py-1 rounded border border-gray-200 hover:bg-gray-200 truncate max-w-[200px]"
                    >
                      {s.url}
                    </a>
                  ))}
                </div>
              </div>
            )}

            {draft.status === "Pending" && (
              <div className="flex gap-4 mt-6">
                <button
                  onClick={approve}
                  className="bg-green-600 text-white px-4 py-2 rounded-lg hover:bg-green-700"
                >
                  {t.approve}
                </button>

                <button
                  onClick={reject}
                  className="bg-red-600 text-white px-4 py-2 rounded-lg hover:bg-red-700"
                >
                  {t.reject}
                </button>
              </div>
            )}

            {draft.status !== "Pending" && (
              <div className="text-sm text-gray-500 mt-4">
                Status: {draft.status}
              </div>
            )}
          </div>
        )}

        {message && (
          <div className="mt-4 text-sm font-medium text-blue-600">
            {message}
          </div>
        )}
      </div>

      {/* Input Bar */}
      <div className="p-4 border-t bg-gray-50">
        <div className="max-w-3xl mx-auto flex gap-2">
          <textarea
            rows={1}
            placeholder={t.placeholder}
            value={question}
            onChange={(e) => setQuestion(e.target.value)}
            className="w-full px-4 py-2 border border-gray-300 rounded-xl focus:ring-2 focus:ring-blue-500 outline-none resize-none text-black"
          />

          <button
            onClick={ask}
            disabled={asking || !question.trim()}
            className="bg-blue-600 text-white px-4 py-2 rounded-xl hover:bg-blue-700 disabled:bg-gray-300"
          >
            {t.create}
          </button>
        </div>

        <p className="text-[10px] text-center mt-2 text-gray-400">
          {t.footer}
        </p>
      </div>
    </div>
  );
}
